<p>The sitemap should describe the relations between the main areas of your website. Such representation could help understand how usable the final product will be. <p> 
<p> It can show the developer the “relationship” between the different pages of a website, so he can judge how easy it will be for the end-user to find the required information or service if he starts from the main page. </p>
<strong>The main reason behind the sitemap creation is to build a user-friendly and easy to navigate website.</strong>
    <p>The sitemap allows the developer to understand how the inner structure of a website looks like but doesn’t describe the user interface. </p>
<p>In this phase, a wireframe or mock-up is created. A wireframe is a visual representation of the user interface that the developer will be going to create. </p>
<strong>But it doesn’t contain any design elements such as colors, logos, etc. It only describes the elements that will be added to the page and their location. </strong>
<p>It’s artless and cheap in production sketch.</p>
